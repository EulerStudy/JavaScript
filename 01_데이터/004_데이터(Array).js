// Array(배열)

// 1) 생성자 함수
const fruitsA = new Array('Apple', 'Banana', 'Cherry')
console.log(fruitsA)   // ['Apple', 'Banana', 'Cherry']


// 2) 배열 리터럴
const fruitsB = ['Apple', 'Banana', 'Cherry']
console.log(fruitsB)   // ['Apple', 'Banana', 'Cherry']

console.log(fruitsB[1])   // 인덱싱, 대괄호표기법,
console.log(fruitsB.length) // 아이템의 개수 조회
