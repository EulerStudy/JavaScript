// 부정(Negation)

console.log(!true)    // false
console.log(!false)   // true
console.log(!0)       // true

// 비교(Comparison)

const a = 1
const b = 3
console.log(a == b)     // false(동등연산자)
console.log(a != b)     // true
console.log(a === b)    // false(일치연산자)
console.log(a !== b)    // true
console.log(a > b)      // false
console.log(a >= b)     // false
console.log(a < b)      // true
console.log(a <= b)    // false



